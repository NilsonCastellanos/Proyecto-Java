    public void Graficar() {
        Connection conexion = null;
        conexion = ConexionBD.conectar();
        PreparedStatement ps;
        ResultSet rs;

        try {
            String sql = "select stock , nombre from producto ";
            ps = conexion.prepareStatement(sql);
            rs = ps.executeQuery();
            DefaultCategoryDataset dataset = new DefaultCategoryDataset();

            while (rs.next()) {
                dataset.addValue(rs.getInt(1), rs.getString(2), rs.getString(2));

            }

            JFreeChart jf = ChartFactory.createBarChart3D("Stock de productos en el Frigobar: ", "nombre", "stock", dataset);
            ChartFrame f = new ChartFrame("Stock del Frigobar ", jf);
            //f.setSize(1250,700);
            f.setSize(800, 600);
            f.setLocationRelativeTo(null);
            f.setVisible(true);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

