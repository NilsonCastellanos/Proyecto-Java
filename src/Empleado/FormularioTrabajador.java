    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        if (txtnombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un Nombre para el Trabajador");
            txtnombre.requestFocus();
            return;
        }
        if (txtapellido.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un apellido para el Trabajador");
            txtapellido.requestFocus();
            return;
        }

        if (cbotipo_documento.getSelectedItem().equals("-Seleccionar-")) {

            JOptionPane.showMessageDialog(null, "Seleccione tipo de documento ");
            return;
        }

        if (txtnum_documento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Número de Doc para el Trabajador");
            txtnum_documento.requestFocus();
            return;
        }
        if (txtdireccion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar dirección");
            txtnum_documento.requestFocus();
            return;
        }
        if (txttelefono.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar teléfono");
            txtnum_documento.requestFocus();
            return;
        }
        if (txtemail.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar email");
            txtnum_documento.requestFocus();
            return;
        }

        if (cbocargo.getSelectedItem().equals("-Seleccionar-")) {

            JOptionPane.showMessageDialog(null, "Seleccione tipo de cargo ");
            return;
        }

        if (txtsueldo.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Sueldo para el trabajador");
            txtsueldo.requestFocus();
            return;
        }
        
     

        DTrabajador dts = new DTrabajador();
        CRUDTrabajador func = new CRUDTrabajador();

        dts.setNombre(txtnombre.getText());

        dts.setApellido(txtapellido.getText());
        int seleccionado = cbotipo_documento.getSelectedIndex();
        dts.setTipo_documento((String) cbotipo_documento.getItemAt(seleccionado));
        dts.setNum_documento(txtnum_documento.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());

        dts.setSueldo(Double.parseDouble(txtsueldo.getText()));
        seleccionado = cbocargo.getSelectedIndex();
        dts.setCargo((String) cbocargo.getItemAt(seleccionado));
       
        if (accion.equals("Guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(null, "el trabajador fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();

            }

        } 
        else if (accion.equals("Guardar")) {
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Trabajador fue Guardado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

