    private void jTablelistaTrabajadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablelistaTrabajadoresMouseClicked
   
        btnguardar.setText("Guardar");
        habilitar();
        btneliminar.setEnabled(true);
        accion="Guardar";

        int fila = jTablelistaTrabajadores.rowAtPoint(evt.getPoint());

        txtidpersona.setText(jTablelistaTrabajadores.getValueAt(fila, 0).toString());
        txtnombre.setText(jTablelistaTrabajadores.getValueAt(fila, 1).toString());
        txtapellido.setText(jTablelistaTrabajadores.getValueAt(fila, 2).toString());
        cbotipo_documento.setSelectedItem(jTablelistaTrabajadores.getValueAt(fila, 3).toString());
        txtnum_documento.setText(jTablelistaTrabajadores.getValueAt(fila, 4).toString());
        txtdireccion.setText(jTablelistaTrabajadores.getValueAt(fila, 5).toString());
        txttelefono.setText(jTablelistaTrabajadores.getValueAt(fila, 6).toString());
        txtemail.setText(jTablelistaTrabajadores.getValueAt(fila, 7).toString());
        cbocargo.setSelectedItem(jTablelistaTrabajadores.getValueAt(fila, 8).toString());
        txtsueldo.setText(jTablelistaTrabajadores.getValueAt(fila, 9).toString());
        
    }//GEN-LAST:event_jTablelistaTrabajadoresMouseClicked

